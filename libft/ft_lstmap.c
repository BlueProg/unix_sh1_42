#include "libft.h"

t_list		*ft_lstmap(t_list *lst, t_list *(*f)(t_list *elem))
{
	t_list	*map;

	map = NULL;
	while (lst != NULL && f)
	{
		ft_lstaddlast(&map, (*f)(lst));
		lst = lst->next;
	}
	return (map);
}
